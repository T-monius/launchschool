# There are a number of variables listed below. What are the different
# types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# Answer:
# The first variable is a local variable because it is not preceded by
# a special character and is all lowercased.
# The second variable is an instance variable because it is preceded by
# an @ sign while the rest of the variable being lowercased.
# The third variable is preceded by two @ signs and the rest is lower-
# cased, so it is a class variable.

