# What are the benefits of using Object Oriented Programming in Ruby?
# Think of as many as you can.

# Answer:
# Object Oriented programming contributes to easier to read code when the
# complex procedural aspects of the code are broken down into pieces and 
# grouped in classes and methods that refer specifically to that
# functionality.
# Breaking up the pieces of code and hiding them allows for data
# protection.
# Thinking and planning the code in OO fashion allows for the programmer
# to work at a higher level of abstraction.
# Easier debugging
# Easier changes and updates
# Simpler collaboration as a colleague or future developer may only need
# to reference one bit of the code.
